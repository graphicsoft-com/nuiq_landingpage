            <?php
            require 'vendor/autoload.php';

            $Parsedown = new Parsedown();

            $articlesDir = 'articles/';

            // Get all .md files in the articles directory
            $articles = glob($articlesDir . '*.md');

            // Sort articles by modification time, newest first
            usort($articles, function($a, $b) {
                return filemtime($b) - filemtime($a);
            });

            // Loop through the first four articles
            for ($i = 0; $i < min(4, count($articles)); $i++) {
                // Get the article content and convert it to HTML
                $articleContent = file_get_contents($articles[$i]);
                $articleHtml = $Parsedown->text($articleContent);
                $articleName = basename($articles[$i]);

                // Extract the first paragraph
                $paragraphs = explode('</p>', $articleHtml, 2);
                $intro = $paragraphs[0] . '</p>';

                echo '<div class="column">';
                echo $intro;
                echo "<a href=\"article.php?article={$articleName}\">Read more</a>";
                echo '</div>';
            }
            ?>
        </div>
